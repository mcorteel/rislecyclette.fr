<h2 class="my-3">Règlement intérieur de la Rislecyclette</h2>

<h3><a id="philosophie">1. Philosophie des ateliers</a></h3>

<p>Le but de l’association est de favoriser l’usage du vélo comme mode de déplacement, en mettant à disposition de ses adhérents des ateliers d’entretien et d’auto-réparation de vélo et plus généralement de véhicule à propulsion humaine. Solidaires et coopératifs, les ateliers sont des lieux de transfert de connaissances et valorisent l'entraide dans l'apprentissage de la mécanique cycle.</p>
<p>Ce sont aussi des lieux de recyclage et de revalorisation des vélos ainsi que des espaces de partage et de convivialité.</p>
<p>Dans nos atelier, le respect, l'écoute et l'entraide sont promus.</p>
<p>Le sexisme, le racisme et l’âgisme ne sont pas tolérés.</p>

<h3><a id="acces">2. Accès aux ateliers</a></h3>
<p>Les locaux sont ouverts aux jours et heures déterminés par le collège de l’association. Seuls les adhérents à jour de leur cotisation et munis de leur carte peuvent utiliser les services d’un atelier.</p>
<p>Les mineurs ne peuvent pas utiliser l’atelier sans être accompagnés d’un responsable légal, sauf avec décharge signée d’un responsable légal et accord d’un permanent présent.</p>
<p>La carte d'adhérent peut être demandée à tout moment lors d'une permanence. Elle ne peut pas être partagée, sauf avec un autre membre de la famille si une adhésion « familiale » est souscrite.</p>
<p>L’adhésion est à prix libre.</p>
<p>Les animateurs ont le droit de refuser l’accès à quiconque ne respecte pas le règlement intérieur.</p>

<h3><a id="usage">3. Usage des ateliers</a></h3>
<p>Les adhérents peuvent utiliser les ateliers et tout ce qui les compose à condition de respecter les règles suivantes :</p>
<ol>
    <li>Respecter et veiller sur les autres adhérents.</li>
    <li>Respecter, partager et ranger chaque outil après utilisation.</li>
    <li>Après vérification de l’adhésion, il sera proposé à l’adhérent un diagnostic pour son vélo effectué avec l’aide des animateurs référents de l’atelier. Une fois les réparations effectuées, il doit s’acquitter de l’achat des pièces détachées prises (neuves ou d’occasions) avant son départ de l’atelier.</li>
    <li>Les vélos ne peuvent être entreposés dans un atelier qu’avec l’accord d’un animateur référent et en fonction de la place disponible. Les vélos entreposés doivent être étiquetés.</li>
    <li>Respecter les consignes des animateurs référents de l’atelier durant la permanence concernant l'animation de cette dernière (horaires d'ouverture, désignation du poste de travail, etc.).</li>
    <li>Ne pas utiliser les services de l'atelier ni les activités de l'association dans un but commercial.</li>
    <li>Respecter le tri des déchets.</li>
    <li>Ranger et nettoyer l'atelier à la fin de chaque permanence.</li>
</ol>

<h3><a id="vente">4. La vente de pièces et vélos</a></h3>
<p><strong>Pièces détachées d’occasion :</strong> elles sont délivrées par un animateur référent à un prix modéré libre, dans une fourchette déterminée par le collège pour tous les ateliers.</p>
<p><strong>Pièces neuves :</strong> elles sont délivrées par un animateur référent à un prix fixe, déterminé par le collège pour tous les ateliers.</p>
<p><strong>Vélos :</strong> les vélos ne peuvent être vendus qu’aux adhérents. Un vélo réservé est un vélo acheté.</p>
<p><strong>Réservation :</strong> tout ce qui porte une étiquette avec un nom ne peut être vendu.</p>

<h3><a id="responsabilites">5. Responsabilités</a></h3>
<ol>
    <li>Chaque adhérent est responsable des réparations ou des modifications apportées sur le vélo sur lequel il travaille.</li>
    <li>Les permanences d’atelier sont animées par des référents bénévoles ou par un salarié de l'association. L’association n’a pas d’obligation de résultat et décline toute responsabilité en cas de désagrément résultant d'une réparation effectuée par un adhérent ou un salarié.</li>
    <li>L'association ne peut être tenue responsable des dommages causés à un adhérent par un permanent ou par un élément mobilier ou immobilier de l'atelier, sauf en cas de comportement à risque.</li>
    <li>Au sein de l’atelier, pendant les heures d’ouvertures, la consommation d'alcool est interdite.</li>
</ol>

<h3><a id="animateurs">6. Animateurs référents</a></h3>
<p>Les animateurs référents de l'atelier sont les bénévoles ou les salariés désignés par le collège de l'association.</p>
